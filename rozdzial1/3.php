    <p>arytmetyczne</p>
    <br> ” + ” zwraca sumę dwóch liczb lub ciągów,
    <br> ” – ” zwraca różnicę, ” * ” zwraca iloczyn,
    <br> ” / ” zwraca iloraz,
    <br> ” % ” zwraca resztę z dzielenia.

    <p>porównania</p>
    <br> ” == ” sprawdza, czy dwie zmienne są równe, co do wartości,
    <br> ” != ” sprawdza, czy zmienne są różne co do wartości,
    <br> ” === ” sprawdza, czy zmienne są identyczne,
    <br> ” !== ” sprawdza, czy zmienne są nieidentyczne,
    <br> ” > ” sprawdza, czy zmienna z lewej strony jest większa od zmiennej z prawej strony (analogicznie w drugą stronę),
    <br> ”>= ” sprawdza, czy zmienna z lewej strony jest większa bądź równa od zmiennej z prawej strony (analogicznie).

    <p>logiczne</p>
    <br> ” ! ” operator zaprzeczenia (logiczne NOT),
    <br> ” && ” operator koniunkcji (logiczne AND),
    <br> ” || ” operator alternatywy (logiczne OR).

    <p>operatory inkrementacji i dekrementacji</p>
    <br> ” $i++ ” postinkrementację, zwiększa wartość zmiennej o 1,
    <br> ” ++$i ” preinkrementację, zwiększa wartość zmiennej o 1,
    <br> ” $i– ” postdekrementację, zmniejsza wartość zmiennej o 1,
    <br> ” –$i ” predekrementację, zmniejsza wartość zmiennej o 1.

    <p>przypisania</p>
    <br> ” $i+=5 ” zwiększenie wartości o 5,
    <br> ” $i-=5 ” zmniejszenie wartości o 5,
    <br> ” $i*=5 ” przypisanie wartości 5 razy większej,
    <br> ” $i/=5 ” przypisanie wartości 5 razy mniejszej,
    <br> ” $i%=5 ” przypisanie wartości reszty z dzielenia zmiennej przez 5.

    <p>obsługi błędów</p>
    <br> ” @ ” ukrycie komunikatu o błędach

